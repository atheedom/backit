'<h3>The Inter-Island Artist Lock-In Project Concept</h3>
<p>The Artist Lock-In is a professionally facilitated residency project that brings Artists of a variety of disciplines, ages, backgrounds and experiences together to learn from each other and respond to their surroundings. Its aim is to provide Artists with time and space to consider new ways of working and collaborate with people of different practices.</p>
<p>Participants spend a fixed period of time (previously it has been a week) in an unusual location and are taken through a series of tasks that encourage teamwork and creative thinking. As the week progresses the tasks become more open, until the participants are left to work together to make shared work that will be showcased to an audience at a set time at the end of the residency.</p>
<p>To mirror the inclusive and cooperative ethos of the Island Games, the JAT is  inviting one Artist from each participating Island to spend 10 days at Elizabeth Castle where they will develop shared work around the theme of ''Islandness''. As an Island off an Island, Elizabeth Castle is the perfect location for such exploration to take place. For up to 12 hours a day it is completely cut off from the mainland of Jersey and contains a myriad of interesting spaces and structures.</p>
<p>Over the course of 8 days the Artists will work together to create a live installation that can then be showcased to the public in 3 ''performances'' over the remaining 2 days. The project will be fully residential with no Artist leaving the Island of Elizabeth Castle at any point in the project – they will truly be ''locked in''.</p>
<p>The aim of the project is to bring people of different cultures, languages, experiences and backgrounds together in a spirit of creativity and cooperation, forming closer ties with our fellow Islands.</p>

