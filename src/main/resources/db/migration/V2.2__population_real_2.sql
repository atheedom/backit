<p>The remit of the Jersey Arts Trust (JAT) is to develop the arts in Jersey. Along with a small grants scheme and a programme of initiatives designed to support the local arts community, JAT has in recent years built a reputation for large-scale development projects that encourage the creation of new work by local and international Artists. Programmes such as the Spearpoint New Plays Project, Skipton Open Studios, Bedell Creative Arts Programme, Artist Residencies and Artist Lock In have all provided a professionally facilitated framework for Artists at all levels of ability, backgrounds and disciplines to develop their craft and create exciting new work.</p>
